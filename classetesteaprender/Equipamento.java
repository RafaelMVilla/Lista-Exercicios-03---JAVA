package classetesteaprender;

public class Equipamento {
    
    public String nome;
    public double dano;
    public double armadura;
    public double forca;

    public Equipamento (String nome, double dano, double armadura, double forca){

        this.nome = nome;
        this.dano = dano;
        this.armadura = armadura;
        this.forca = forca;
    }
}