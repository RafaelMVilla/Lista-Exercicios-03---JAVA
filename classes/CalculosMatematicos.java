package classes;

public class CalculosMatematicos {
    

    //--------------2 NUMEROS-------------------//

    public double numero1;
    public double numero2;

    // SOMA
    public double calculoSomar(){

        double resultado = numero1 + numero2;

        return resultado;
    }

    // MULTIPLICAR
    public double calculoMultiplicar(){

        double resultado = numero1 * numero2;

        return resultado;
    }

    
    public double calculoSomar(double x, double y){

        double resultado = x + y;

        return resultado;
    }

    // MULTIPLICAR
    public double calculoMultiplicar(double x, double y){

        double resultado = x * y;

        return resultado;
    }
    
}
